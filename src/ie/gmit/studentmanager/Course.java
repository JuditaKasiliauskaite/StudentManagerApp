package ie.gmit.studentmanager;

public class Course {
	private String courseName;
	private int level;

	public String getCourseName() {
		return courseName;
	}

	public void setCourseName(String newCourseName) {
		courseName = newCourseName;
	}

	public int getLevel() {
		return level;
	}

	public void setLevel(int newLevel) {
		level = newLevel;
	}

}
